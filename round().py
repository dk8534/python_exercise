'''Round() : round function is used
 for round the floating point number
  to a specific decimal ponit.'''

"Syntax:round(number,ndigits)"

'''number -> the number to be rounded off 
   ndigits -> the number of decimal places'''

example: print(round(3.14159265389,2))

"or"

pi=3.14159265389
print(round(pi,2))
print(round(pi,3))

"or"

pi=3.14159265389
two_decimals=round(pi,2)
three_decimals=round(pi,3)

print(two_decimals)
print(three_decimals)